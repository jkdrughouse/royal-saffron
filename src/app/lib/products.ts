function replaceJehlumWithJhelum(value: string): string {
    return value.replace(/jehlum/gi, "Jhelum");
}

function normalizeProductText(product: Product): Product {
    const normalize = (value: string | undefined) =>
        typeof value === "string" ? replaceJehlumWithJhelum(value) : value;

    return {
        ...product,
        name: normalize(product.name) ?? product.name,
        category: normalize(product.category) ?? product.category,
        description: normalize(product.description) ?? product.description,
        detailedDescription: normalize(product.detailedDescription),
        painPointHeadline: normalize(product.painPointHeadline),
        sensoryDescription: normalize(product.sensoryDescription),
        benefits: product.benefits?.map((b) => ({
            ...b,
            title: replaceJehlumWithJhelum(b.title),
            description: replaceJehlumWithJhelum(b.description),
        })),
        images: product.images?.map((img) => ({
            ...img,
            alt: replaceJehlumWithJhelum(img.alt),
            caption: normalize(img.caption),
        })),
    };
}

function applySaffronDiscount(product: Product): Product {
    if (product.id !== "kashmiri-saffron") return product;

    const DISCOUNT = 0.2;
    const applyDiscount = (original: number, current?: number) => {
        const base = original;
        const currentPrice = current && current < base ? current : Math.round(base * (1 - DISCOUNT));
        return { original: base, current: currentPrice };
    };

    const base = applyDiscount(product.originalPrice ?? product.price, product.price);

    return {
        ...product,
        price: base.current,
        originalPrice: base.original,
        variants: product.variants?.map((variant) => {
            const v = applyDiscount(variant.originalPrice ?? variant.price, variant.price);
            return {
                ...variant,
                price: v.current,
                originalPrice: v.original,
            };
        }),
        trustBadges: Array.from(
            new Set([...(product.trustBadges ?? []), "limited-offer-20%-off"])
        ),
    };
}

export const products: Product[] = rawProducts.map(normalizeProductText).map(applySaffronDiscount);
